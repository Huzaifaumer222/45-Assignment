let x = 5;
let y = 10;

// Test 1: Is x equal to 5? Prediction: True
console.log("Is x == 5? I predict True.");
console.log(x == 5); // True

// Test 2: Is y equal to 10? Prediction: True
console.log("Is y == 10? I predict True.");
console.log(y == 10); // True

// Test 3: Is x not equal to 10? Prediction: True
console.log("Is x != 10? I predict True.");
console.log(x != 10); // True

// Test 4: Is y greater than x? Prediction: True
console.log("Is y > x? I predict True.");
console.log(y > x); // True

// Test 5: Is y less than x? Prediction: False
console.log("Is y < x? I predict False.");
console.log(y < x); // False

// Test 6: Is x less than or equal to 5? Prediction: True
console.log("Is x <= 5? I predict True.");
console.log(x <= 5); // True

// Test 7: Is y greater than or equal to 10? Prediction: True
console.log("Is y >= 10? I predict True.");
console.log(y >= 10); // True

// Test 8: Is x equal to 10 OR y equal to 10? Prediction: False
console.log("Is x == 10 OR y == 10? I predict False.");
console.log(x == 10 || y == 10); // False

// Test 9: Is x equal to 5 AND y equal to 10? Prediction: True
console.log("Is x == 5 AND y == 10? I predict True.");
console.log(x == 5 && y == 10); // True

// Test 10: Is x not equal to 5 OR y not equal to 10? Prediction: False
console.log("Is x != 5 OR y != 10? I predict False.");
console.log(x != 5 || y != 10); // False
